import Foundation
import HMUtilities
import XCTest
@testable import AutoAPI


final class AAMessagingTests: XCTestCase {

    // MARK: State Properties
    
    func testText() {
        let bytes: [UInt8] = [0x0d, 0x00, 0x37, 0x01, 0x01, 0x00, 0x0b, 0x01, 0x00, 0x08, 0x48, 0x65, 0x79, 0x20, 0x6d, 0x6f, 0x6d, 0x21]
        
        guard let capability = try? AAAutoAPI.parseBytes(bytes) as? AAMessaging else {
            return XCTFail("Could not parse bytes as `AAMessaging`")
        }
        
        XCTAssertEqual(capability.text?.value, "Hey mom!")
    }
    
    func testHandle() {
        let bytes: [UInt8] = [0x0d, 0x00, 0x37, 0x01, 0x02, 0x00, 0x06, 0x01, 0x00, 0x03, 0x45, 0x70, 0x70]
        
        guard let capability = try? AAAutoAPI.parseBytes(bytes) as? AAMessaging else {
            return XCTFail("Could not parse bytes as `AAMessaging`")
        }
        
        XCTAssertEqual(capability.handle?.value, "Epp")
    }


    // MARK: Setters
    
    func testMessageReceived() {
        let bytes: [UInt8] = [0x0d, 0x00, 0x37, 0x01, 0x01, 0x00, 0x0b, 0x01, 0x00, 0x08, 0x48, 0x65, 0x79, 0x20, 0x6d, 0x6f, 0x6d, 0x21, 0x02, 0x00, 0x06, 0x01, 0x00, 0x03, 0x45, 0x70, 0x70]
        let setterBytes = AAMessaging.messageReceived(handle: "Epp", text: "Hey mom!")
        
        XCTAssertEqual(bytes, setterBytes)
    }


    // MARK: Identifiers
    
    func testCapabilityIdentifier() {
        XCTAssertEqual(AAMessaging.identifier, 0x0037)
    }
    
    func testPropeertyIdentifiers() {
        XCTAssertEqual(AAMessaging.PropertyIdentifier.text.rawValue, 0x01)
        XCTAssertEqual(AAMessaging.PropertyIdentifier.handle.rawValue, 0x02)
    }
}
